export function meta() {
  return [
    { title: "CivicLens" },
    { name: "description", content: "CivicLens is a web application" },
  ];
}

export default function Home() {
  const [healthCheck, setHealthCheck] = useState({ isLoading: true, data: false });
  useEffect(() => {
    let active = true;
    fetch(`${env.VITE_SERVER_URL}/health`)
      .then((response) => {
        if (active) setHealthCheck({ isLoading: false, data: response.ok });
      })
      .catch(() => {
        if (active) setHealthCheck({ isLoading: false, data: false });
      });
    return () => {
      active = false;
    };
  }, []);

  return (
    <div className="container mx-auto max-w-3xl px-4 py-2">
      <pre className="overflow-x-auto font-mono text-sm">{TITLE_TEXT}</pre>
      <div className="grid gap-6">
        <section className="rounded-lg border p-4">
          <h2 className="mb-2 font-medium">API Status</h2>
          <div className="flex items-center gap-2">
            <div
              className={`h-2 w-2 rounded-full ${healthCheck.data ? "bg-green-500" : "bg-red-500"}`}
            />
            <span className="text-sm text-muted-foreground">
              {healthCheck.isLoading
                ? "Checking..."
                : healthCheck.data
                  ? "Connected"
                  : "Disconnected"}
            </span>
          </div>
        </section>
      </div>
    </div>
  );
}
